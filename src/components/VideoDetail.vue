<template>
  <div v-if="video" class="col-md-8">
    <div class="embed-responsive embed-responsive-16by9">
      <iframe :src="getEmbedUrl" class="embed-responsive-item"></iframe>
    </div>
    <div class="details">
      <h4>{{getTitle}}</h4>
      <p>{{getDescription}}</p>
    </div>
  </div>
</template>

<script>
export default {
  name: "VideoDetail",
  props: ["video"],
  computed: {
    getTitle() {
      return this.video.snippet.title;
    },
    getDescription() {
      return this.video.snippet.description;
    },
    getEmbedUrl() {
      const videoId = this.video.id.videoId;
      return `https://www.youtube.com/embed/${videoId}`;
    }
  }
};
</script>

<style scoped>
.details {
  margin-top: 10px;
  padding: 10px;
  border: 1px solid #ddd;
  border-radius: 4px;
}
</style>